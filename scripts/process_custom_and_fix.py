# ============================================================
# 3. 分类（使用新的ALL_CUSTOM_IDS）
# ============================================================
custom_link = []
size_miss = []
pingka = []
no_match = []

# 记录统计
stats = {'珍珠棉': 0, '蓝绿颜色': 0, '定制关键词': 0, '手动指定_新增': 0, '手动指定_原有': 0}
new_custom_from_size = 0
new_custom_from_pingka = 0

for r in rows:
    if not r:
        continue
    reason = str(r[4] or '').strip()
    spec_id = str(r[2] or '').strip()
    
    if reason in ('珍珠棉', '蓝绿颜色', '定制关键词'):
        custom_link.append(r)
        stats[reason] += 1
    elif reason == '尺寸不足':
        if spec_id in ALL_CUSTOM_IDS:
            custom_link.append(r)
            if spec_id in CUSTOM_IDS:
                new_custom_from_size += 1
                stats['手动指定_新增'] += 1
            else:
                stats['手动指定_原有'] += 1
        else:
            size_miss.append(r)
    elif reason == '平卡/解析失败':
        if spec_id in ALL_CUSTOM_IDS:
            custom_link.append(r)
            if spec_id in CUSTOM_IDS:
                new_custom_from_pingka += 1
                stats['手动指定_新增'] += 1
            else:
                stats['手动指定_原有'] += 1
        else:
            pingka.append(r)
    else:
        no_match.append(r)

