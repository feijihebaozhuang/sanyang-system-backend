def simplify(code):
    """10*10*10-外径-白色 → 101010"""
    m = re.match(r'^([\d.*×xX]+)', code)
    if not m: return code
    parts = re.split(r'[*×xX]', m.group(1))
    chunks = []
    for p in parts:
        d = re.sub(r'\D', '', p)
        if d: chunks.append(d)
    return ''.join(chunks) if chunks else code

