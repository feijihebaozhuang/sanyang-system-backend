# -*- coding: utf-8 -*-
import re

s1 = '宽【39cm】高【7cm】;【100个】长【39cm】'
s2 = '宽【12cm】高【12cm】;【100个】长度【12cm】'

# v9 A1 格式
m = re.search(r'宽[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]'
              r'.*?'
              r'高[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]'
              r'.*?'
              r'(?:长度?)[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]', s1)
print(f's1 A1: {m}')
if m:
    print(f'  宽={m.group(1)}, 宽单位={m.group(2)!r}')
    print(f'  高={m.group(3)}, 高单位={m.group(4)!r}')
    print(f'  长={m.group(5)}, 长单位={m.group(6)!r}')

# 第二种：长度在前（长度【L】而非长【L】）
# 但这里是 【100个】在中间，所以长在最后
# A1用的是非贪婪.*? 应该可以跨过 【100个】
# 让我看看是不是 "长度" 而不是 "长" 的问题
# (?:长度?) 匹配 "长" 或 "长度"

m2 = re.search(r'宽[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]'
               r'.*?'
               r'高[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]'
               r'.*?'
               r'(?:长度?)[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]', s2)
print(f's2 A1: {m2}')
if m2:
    print(f'  宽={m2.group(1)}, 高={m2.group(3)}, 长={m2.group(5)}')
else:
    print('未匹配！检查问题...')
    # s2: 宽【12cm】高【12cm】;【100个】长度【12cm】
    # "长度" 匹配 (?:长度?) 应该ok
    # 让我单独测各个部分
    m_w = re.search(r'宽[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]', s2)
    print(f'  宽部分: {m_w}')
    m_h = re.search(r'高[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]', s2)
    print(f'  高部分: {m_h}')
    m_l = re.search(r'(?:长度?)[【\[]\s*([\d.]+)\s*(\w*)\s*[】\]]', s2)
    print(f'  长部分: {m_l}')
